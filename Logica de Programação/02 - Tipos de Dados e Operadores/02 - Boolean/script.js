console.log("02 - Tipo Boolean");

console.log(10 > 5);
console.log(10 < 5);
console.log(typeof (10 < 5));
console.log(10 == 10); // igualdade
console.log(10 == "10"); // igualdade
console.log(10 === "10"); // comparando o tipo do dado

// Apenas um sinal de "=" significa recebe

console.log(3 != 3);
console.log(3 != 4);

// != significa "diferente"
console.log(3 != "3");

// !== significa "estritamente diferente"
console.log(3 !== "3");

console.log(5 > 4 && 3 == 3);
console.log(5 > 3 || 3 == 2);

console.log(true && true);
console.log(false || false);

console.log(!true);
console.log(!false);
